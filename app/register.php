<?php

require_once "config.php";


$username = $password = $confirm_password = "";
$username_err = $password_err = $confirm_password_err = "";


if ($_SERVER["REQUEST_METHOD"] == "POST") {

    $username_post = trim($_POST["username"] ?? "");
    $password_post = trim($_POST["password"] ?? "");
    $confirm_password_post = trim($_POST["confirm_password"] ?? "");


    if (empty($username_post)) {
        $username_err = "Entrez un nom d'utilisateur.";
    } elseif (!preg_match('/^[a-zA-Z0-9_]+$/', $username_post)) {
        $username_err = "Le nom d'utilisateur ne peut contenir que des chiffres, lettres et underscore.";
    } else {

        $param_password = $username_post;

        $sql = "SELECT Id_Client FROM Client WHERE Identifiant = :username";

        if ($stmt = $pdo->prepare($sql)) {

            $stmt->bindParam(":username", $param_username, PDO::PARAM_STR);


            $param_username = $username_post;


            if ($stmt->execute()) {
                if ($stmt->rowCount() == 1) {
                    $username_err = "Ce Nom d'Utilisateur est déjà pris.";
                } else {
                    $username = $username_post;
                }
            } else {
                error_log("Problème lors de la vérification de l'identifiant: " . $stmt->errorInfo()[2]);
                echo "Problème interne lors de la vérification de l'identifiant.";
            }


            $stmt = null;
        }
    }


    if (empty($password_post)) {
        $password_err = "Entrez un mot de passe.";
    } elseif (strlen($password_post) < 4) {
        $password_err = "Le mot de passe doit contenir au moins 4 caractères.";
    } else {
        $password = $password_post;
    }


    if (empty($confirm_password_post)) {
        $confirm_password_err = "Confirmez le mot de passe.";
    } else {
        $confirm_password = $confirm_password_post;
        if (empty($password_err) && ($password !== $confirm_password)) {
            $confirm_password_err = "Les mots de passe ne correspondent pas.";
        }
    }


    if (empty($username_err) && empty($password_err) && empty($confirm_password_err)) {

        $sql = "INSERT INTO client (Identifiant, password) VALUES (:username, :password)";

        if ($stmt = $pdo->prepare($sql)) {

            $stmt->bindParam(":username", $param_username, PDO::PARAM_STR);
            $stmt->bindParam(":password", $param_password, PDO::PARAM_STR);

            $param_username = $username;

            $param_password = password_hash($password, PASSWORD_DEFAULT);

            if ($stmt->execute()) {

                header("location: login.php");
                exit;
            } else {

                error_log("Problème lors de l'insertion: " . $stmt->errorInfo()[2]);
                echo "Problème interne lors de l'inscription.";
            }

            $stmt = null;
        }
    }


    $pdo = null;
}
